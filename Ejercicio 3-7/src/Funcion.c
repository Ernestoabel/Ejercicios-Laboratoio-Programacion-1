#include <stdio.h>
#include <stdlib.h>

int FuncionSumar(int numero1, int numero2)
{
    int resultadoSuma;
    resultadoSuma=numero1+numero2;
    return resultadoSuma;
}
int FuncionRestar(int numero1, int numero2)
{
    int resultadoResta;
    resultadoResta=numero1-numero2;
    return resultadoResta;
}
